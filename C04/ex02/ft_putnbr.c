#include<stdio.h>
#include<unistd.h>

void	ft_putnbr(int nb)
{
	 if (nb < 0)
	{
		nb = -nb;
	}
	else if (nb >= 10)
	{
		ft_putnbr(nb / 10);
		ft_putnbr(nb % 10);
	}
	else ft_putnbr(nb + 0);
}
